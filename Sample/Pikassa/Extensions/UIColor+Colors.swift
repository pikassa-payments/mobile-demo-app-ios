import UIKit


extension UIColor {
    static func textGray() -> UIColor {
        return UIColor(named: "textGray")!
    }

    static func successGreen() -> UIColor {
        return UIColor(named: "successGreen")!
    }

    static func errorRed() -> UIColor {
        return UIColor(named: "errorRed")!
    }

    static func accentBlue() -> UIColor {
        return UIColor(named: "accentBlue")!
    }

    static func underlineNormal() -> UIColor {
        return UIColor(named: "underlineNormal")!
    }
}
